package com.redice_inc.layoutexamples;


public class Profile {
    String name;
    String company;
    String job;
    String profile_image;

    Profile(String name, String company, String job, String profile_image) {
        this.name = name;
        this.company = company;
        this.job = job;
        this.profile_image = profile_image;
    }
}
